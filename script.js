document.getElementById("call1").addEventListener("click", function(event) {
    event.preventDefault();
    const coinCount = document.getElementById("coinCount");
    if (parseInt(coinCount.innerText) > 0) {
        coinCount.innerText = parseInt(coinCount.innerText) - 20;
        alert("Calling National Emergency Service 999");
    }
    else {
        alert("Not enough coins to make a call you need at least 20 coins.");
    }
});


